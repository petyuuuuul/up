DROP TABLE [TaskChapter]
IF OBJECT_ID (N'[TaskChapter]', N'U') IS NULL CREATE TABLE [TaskChapter]
(
    [ChapterId] INT NOT NULL UNIQUE,
	[ChapterClass] INT NOT NULL,
	[ChapterCode] INT NOT NULL,
	[ChapterName] [nvarchar] (255) NOT NULL,
	[ChapterDesc] [nvarchar] (255) NULL,
	[CreateDateTime] datetime default GETDATE()

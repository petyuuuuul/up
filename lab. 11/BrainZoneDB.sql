CREATE TRIGGER [AlterUser] 
ON [TaskSetting]
AFTER INSERT
AS 
BEGIN
	SET NOCOUNT ON
	DECLARE @Temp nvarchar(5)
	DECLARE @PRC2 nvarchar(max)
	SET @Temp = CAST((SELECT [TaskId] FROM inserted) AS CHAR (5))
	IF NOT EXISTS(SELECT * FROM sys.columns WHERE [object_id] = OBJECT_ID('[User]') AND [name] = @Temp)
	BEGIN
		ALTER TABLE [User] ADD [@Temp] nvarchar(5)
		SET @PRC2 = 'EXECUTE sp_rename @objname = [User.@Temp], @newname='''+@Temp+''', @objtype=''COLUMN'''
		EXEC(@PRC2)
	END 
END


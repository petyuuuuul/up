DROP TABLE [TaskSetting]
IF OBJECT_ID (N'[TaskSetting]', N'U') IS NOT NULL  CREATE TABLE  [TaskSetting]
(
    [TaskId] INT NOT NULL UNIQUE,
	[TaskClass] INT NOT NULL,
	[TaskChapterCode] INT NOT NULL,
	[TaskTitle] [nvarchar] (255) NOT NULL,
	[TaskDesc] [nvarchar] (max) NOT NULL,
	[TaskPict] [nvarchar] (255) NULL,
	[TaskAnswer] [nvarchar] (255) NOT NULL,

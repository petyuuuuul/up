(
    [BookId] INT NOT NULL UNIQUE,
	[TaskClass] INT NOT NULL,
	[BookCode] INT NOT NULL,
	[BookName] [nvarchar] (255) NOT NULL,
	[BookDesc] [nvarchar] (255) NOT NULL,
	[BookAuthors] [nvarchar] (255) NOT NULL,
	[BookCost] [nvarchar] (255) NOT NULL,
	[CreateDateTime] datetime default GETDATE()
);

DROP TABLE [User]
